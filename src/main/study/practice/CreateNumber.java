package main.study.practice;

public class CreateNumber {
	
	/**
	 * 로또복권 기준
	 * count = 6
	 * max = 45
	 */
	
	private static final int count = 5;		// 생성 숫자의 개수
	private static final int max = 8;		// 생성 숫자의 최대값
	
	/**
	 * @return the count
	 */
	public final int getCount() {
		return count;
	}
	/**
	 * @return the max
	 */
	public final int getMax() {
		return max;
	}
	
}

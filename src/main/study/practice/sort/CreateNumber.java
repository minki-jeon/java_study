public class CreateNumber {
	
	private static final int count = 50;		// 생성 숫자의 개수
	private static final int max = 100;			// 생성 숫자의 최대값
	
	/**
	 * @return the count
	 */
	public final int getCount() {
		return count;
	}
	/**
	 * @return the max
	 */
	public final int getMax() {
		return max;
	}
	
}
